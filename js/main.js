const navToggle = document.querySelector("[data-menu-toggle]");
const navLinks = document.querySelector("[data-nav-links]");
const year = document.querySelector("[data-year]");
const langButtons = document.querySelectorAll("[data-lang-switch]");

if (year) year.textContent = new Date().getFullYear();

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.en;
  document.documentElement.lang = lang;
  document.documentElement.dataset.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-schema]").forEach((image) => {
    const key = image.dataset.schema;
    const src = schemaSources[lang] && schemaSources[lang][key];
    if (src) image.src = src;
    if (schemaAlts[lang] && schemaAlts[lang][key]) image.alt = schemaAlts[lang][key];
  });

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langSwitch === lang);
  });

  localStorage.setItem("flowFundamentalsLang", lang);
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.langSwitch));
});

if (navToggle && navLinks) {
  navToggle.addEventListener("click", () => {
    const isOpen = navLinks.classList.toggle("is-open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      navLinks.classList.remove("is-open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });
}

setLanguage(localStorage.getItem("flowFundamentalsLang") || "en");
